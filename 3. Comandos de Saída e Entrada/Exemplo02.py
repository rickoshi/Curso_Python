A = 12
B = 19
print(A, B)                 # 12 19
print(A, B, sep='-')        # 12-19
print(A, B, sep=' - ')      # 12 - 19
print(A, B, sep='<-->')     # 12<-->19
print(A, B, sep=', ')       # 12, 19

print('um\ndois\ntrês')
# um
# dois
# três

print('um\tdois\ttrês')     # um	dois	três

X = 12
Y = 26
print(X)                # 12
print(Y)                # 26
print(X, end='...')     # 12...
print(Y)                # 26
# 12...26