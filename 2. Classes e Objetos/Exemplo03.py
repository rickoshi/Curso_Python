# Exemplos de objetos de classes simples
n = 12
f = 7.38
c = 1.57 + 3.17j
b = True
a = False
x = None

print(type(n))  # <class 'int'>
print(type(f))  # <class 'float'>
print(type(c))  # <class 'complex'>
print(type(b))  # <class 'bool'>
print(type(a))  # <class 'bool'>
print(type(x))  # <class 'NoneType'>
