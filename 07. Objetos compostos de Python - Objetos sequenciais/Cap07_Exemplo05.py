# Em Python é possível fazer a indexação com números negativos, que permitirá o acesso aos elementos
# de trás para frente, sendo que o índice -1 se refere ao último objeto da lista.

A = [10, 12, 14, 16]
print(A[-1])        # 16
print(A[-2])        # 14
print(A[-3])        # 12
print(A[-4])        # 10
